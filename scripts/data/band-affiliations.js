const starter = require('./starter');
const individualPools = require('./individual-pools');
const extraOverrides = require('./band-overrides-extra');

function normalizeName(name) {
  return String(name).trim().toLowerCase();
}

function collectAllNames() {
  const names = new Set();

  for (const entry of starter) {
    names.add(normalizeName(entry.name));
  }

  for (const eraPools of Object.values(individualPools)) {
    for (const pool of Object.values(eraPools)) {
      for (const name of pool) {
        names.add(normalizeName(name));
      }
    }
  }

  return names;
}

